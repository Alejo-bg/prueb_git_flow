package clases_alejo;

public class PrimeraClase {
    private String cadena;
    private Integer numero;

    public PrimeraClase(String cadena, Integer numero) {

        this.cadena = cadena;
        this.numero = numero;
    }

    public String getCadena() {
        return cadena;
    }

    public Integer getNumero() {
        return numero;
    }

}
